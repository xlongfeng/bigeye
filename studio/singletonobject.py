from PyQt5.QtCore import QObject


class SingletonObject(QObject):
    _pInstance = None

    def __init__(self, parent=None):
        super(SingletonObject, self).__init__(parent)

    @classmethod
    def instance(cls):
        if cls._pInstance is None:
            cls._pInstance = cls()
        return cls._pInstance